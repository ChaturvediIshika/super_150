package Bit_Masking;

public class Shifting {
    public static void main(String[] args) {
        int a=10;
        System.out.println(a>>1);//right shift i.e. divide by 2  // a>>b= a/2^b
        System.out.println(a<<1);//left shift i.e. multiply by 2 //a<<b =a*2^b
        int b=-10;
        System.out.println(b>>1);
        System.out.println(b<<1);
    }
}
